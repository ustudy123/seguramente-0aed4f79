/**
 * Limpa o código CNAE removendo caracteres não numéricos
 */
function cleanCnaeCode(cnae: string): string {
  return cnae.replace(/\D/g, '');
}

/**
 * Busca o Grau de Risco para um código CNAE.
 * Faz busca hierárquica: subclasse (7 dígitos) → subclasse (6 dígitos) → classe (4 dígitos) → divisão (2 dígitos)
 * 
 * @param cnae Código CNAE (qualquer formato: "4120-4/00", "41204", "4120400", "4120", etc.)
 * @returns Grau de Risco (1-4) ou null se não encontrado
 */
export function getGrauRiscoByCnae(cnae: string): number | null {
  const cleaned = cleanCnaeCode(cnae);
  if (cleaned.length < 2) return null;

  // Tentar por subclasse completa (7 dígitos)
  if (cleaned.length >= 7) {
    const sub7 = cleaned.substring(0, 7);
    if (SUBCLASSE_GR[sub7] !== undefined) return SUBCLASSE_GR[sub7];
  }

  // Tentar por subclasse (6 dígitos)
  if (cleaned.length >= 6) {
    const sub6 = cleaned.substring(0, 6);
    if (SUBCLASSE_GR[sub6] !== undefined) return SUBCLASSE_GR[sub6];
  }

  // Tentar por classe (4 dígitos) - buscar no mapa de subclasses as entradas que começam com esses 4 dígitos
  if (cleaned.length >= 4) {
    const classe = cleaned.substring(0, 4);
    // Procurar primeira subclasse que começa com essa classe
    for (const key of Object.keys(SUBCLASSE_GR)) {
      if (key.startsWith(classe)) {
        return SUBCLASSE_GR[key];
      }
    }
  }

  // Fallback por divisão (2 dígitos)
  const divisao = cleaned.substring(0, 2);
  if (DIVISAO_GR[divisao] !== undefined) {
    return DIVISAO_GR[divisao];
  }

  return null;
}

